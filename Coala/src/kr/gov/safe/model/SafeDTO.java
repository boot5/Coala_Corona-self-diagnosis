package kr.gov.safe.model;

public class SafeDTO {
	private String ssido;
	private String ssigungu;
	private String soname;
	private String saddress;
	private String sdiagnosis;
	private String srphone;
	private int num;
	
	
	public String getSsido() {
		return ssido;
	}
	public void setSsido(String ssido) {
		this.ssido = ssido;
	}
	public String getSsigungu() {
		return ssigungu;
	}
	public void setSsigungu(String ssigungu) {
		this.ssigungu = ssigungu;
	}
	public String getSoname() {
		return soname;
	}
	public void setSoname(String soname) {
		this.soname = soname;
	}
	public String getSaddress() {
		return saddress;
	}
	public void setSaddress(String saddress) {
		this.saddress = saddress;
	}
	public String getSdiagnosis() {
		return sdiagnosis;
	}
	public void setSdiagnosis(String sdiagnosis) {
		this.sdiagnosis = sdiagnosis;
	}
	public String getSrphone() {
		return srphone;
	}
	public void setSrphone(String srphone) {
		this.srphone = srphone;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
	
}
